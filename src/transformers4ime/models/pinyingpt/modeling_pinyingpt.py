                gathered_logits = gathered_logits.where(gather_index > 0, torch.ones_like(gathered_logits) * 1e-4)
                # Flatten the tokens
                loss_fct = CrossEntropyLoss()
                loss = loss_fct(gathered_logits.view(-1, gathered_logits.size(-1)), shift_labels.view(-1))
